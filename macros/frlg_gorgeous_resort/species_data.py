# ============================================================
# ダミー判定
# ============================================================

DUMMY_CODE_START: int = 252  # 0x00FC
DUMMY_CODE_END: int = 276    # 0x0114
NUM_SPECIES_CODES: int = 411  # 0x019B (有効コード最大値)


def is_dummy(species_code: int) -> bool:
    """内部コードがダミーエントリかどうかを判定する。"""
    return DUMMY_CODE_START <= species_code <= DUMMY_CODE_END
